let currentMode = "learning";
let currentLevel = "intermediate";
let targetText = "";
let startTime = null;
let timerInterval = null;
let errors = 0;
let timeLeft = 0;
let lastOffsetTop = 0;

const displayEl = document.getElementById("display-text");
const inputEl = document.getElementById("input-area");
const wpmEl = document.getElementById("wpm");
const accuracyEl = document.getElementById("accuracy");
const timerEl = document.getElementById("timer");

function init() {
  populateParaSelect();
  resetSession();
  updateHistoryTable();
}

function toggleTheme() {
  const body = document.body;
  const newTheme = body.getAttribute("data-theme") === "light" ? "dark" : "light";
  body.setAttribute("data-theme", newTheme);
}

function setMode(mode) {
  currentMode = mode;
  document.getElementById('btn-learn').classList.toggle('active', mode === 'learning');
  document.getElementById('btn-practice').classList.toggle('active', mode === 'practicing');
  resetSession();
}

function setLevel(level) {
  currentLevel = level;
  populateParaSelect();
  resetSession();
}

function populateParaSelect() {
  const select = document.getElementById('para-select');
  select.innerHTML = '<option value="random">-- Random Paragraph --</option>';
  content[currentLevel].forEach((p, index) => {
    const opt = document.createElement('option');
    opt.value = index;
    opt.textContent = `Paragraph ${index + 1}: ${p.substring(0, 20)}...`;
    select.appendChild(opt);
  });
}

function loadSpecificPara() {
  const idx = document.getElementById('para-select').value;
  if(idx === "random") {
    const pool = content[currentLevel];
    targetText = pool[Math.floor(Math.random() * pool.length)];
  } else {
    targetText = content[currentLevel][idx];
  }
  applyWordLimit();
  renderText();
}

function applyWordLimit() {
  const limit = parseInt(document.getElementById('word-limit').value);
  if (limit > 0) {
    const words = targetText.split(/\s+/);
    targetText = words.slice(0, limit).join(" ");
  }
}

function renderText() {
  displayEl.innerHTML = targetText.split("").map(char => `<span>${char}</span>`).join("");
  displayEl.scrollTop = 0;
  lastOffsetTop = 0;
}

function resetSession() {
  clearInterval(timerInterval);
  startTime = null;
  errors = 0;
  inputEl.value = "";
  wpmEl.innerText = "0";
  accuracyEl.innerText = "100";

  const selectedTime = parseInt(document.getElementById('time-select').value);
  timeLeft = selectedTime;
  timerEl.innerText = selectedTime > 0 ? formatTime(timeLeft) : "--";

  loadSpecificPara();
}

function formatTime(s) {
  const mins = Math.floor(s / 60);
  const secs = s % 60;
  return `${mins}:${secs.toString().padStart(2, '0')}`;
}

inputEl.addEventListener('keydown', (e) => {
  if (document.getElementById('backspace-select').value === 'off' && e.key === 'Backspace') {
    e.preventDefault();
  }
});

inputEl.addEventListener("input", () => {
  const val = inputEl.value;
  const targetChars = targetText.split("");
  const spans = displayEl.querySelectorAll("span");

  if (!startTime && val.length > 0) {
    startTime = new Date();
    if (timeLeft > 0) {
      timerInterval = setInterval(updateTimer, 1000);
    } else {
      timerInterval = setInterval(updateStatsOnly, 1000);
    }
  }

  errors = 0;
  spans.forEach((span, i) => {
    const typed = val[i];
    if (typed == null) {
      span.className = "";
      if (i === val.length) {
        span.className = "char-current";

        if (span.offsetTop !== lastOffsetTop) {
          displayEl.scrollTo({
            top: span.offsetTop - 60,
            behavior: 'smooth'
          });
          lastOffsetTop = span.offsetTop;
        }
      }
    } else if (typed === targetChars[i]) {
      span.className = "char-correct";
    } else {
      span.className = "char-wrong";
      errors++;
    }
  });

  const accuracy = Math.max(0, Math.round(((val.length - errors) / val.length) * 100)) || 100;
  accuracyEl.innerText = accuracy;

  if (val.length >= targetText.length) finishSession();
});

function updateTimer() {
  timeLeft--;
  timerEl.innerText = formatTime(timeLeft);
  updateStatsOnly();
  if (timeLeft <= 0) finishSession();
}

function updateStatsOnly() {
  const timeElapsed = (new Date() - startTime) / 60000;
  const words = inputEl.value.length / 5;
  if (timeElapsed > 0) {
    wpmEl.innerText = Math.round(words / timeElapsed);
  }
}

function finishSession() {
  clearInterval(timerInterval);
  if (currentMode === "practicing") {
    saveHistory(wpmEl.innerText, accuracyEl.innerText);
  }
  alert(`Test Complete!\nWPM: ${wpmEl.innerText}\nAccuracy: ${accuracyEl.innerText}%`);
  resetSession();
}

function saveHistory(wpm, acc) {
  const history = JSON.parse(localStorage.getItem("hindi_typing_history") || "[]");
  history.push({
    date: new Date().toLocaleDateString(),
    level: currentLevel,
    wpm: wpm,
    acc: acc,
  });
  localStorage.setItem("hindi_typing_history", JSON.stringify(history));
  updateHistoryTable();
}

function updateHistoryTable() {
  const history = JSON.parse(localStorage.getItem("hindi_typing_history") || "[]");
  document.getElementById('history-body').innerHTML = history.reverse().slice(0, 5).map(row => `
    <tr>
      <td>${row.date}</td>
      <td>${row.level.toUpperCase()}</td>
      <td>${row.wpm}</td>
      <td>${row.acc}%</td>
    </tr>
  `).join("");
}

function clearHistory() {
  localStorage.removeItem("hindi_typing_history");
  updateHistoryTable();
}

